#!/usr/bin/env python3
"""v6 — TRUE cross-vendor judge. GPT-4o (OpenAI) blind-judges the FIXED v2
answers (generated by Opus 4.8). Closes the self-preference threat: if a
different VENDOR's model reproduces best > context > psyche > naked, the
finding isn't Anthropic self-preference.

Reads results/raw/results-v2.json; writes results/raw/results-v6-crossvendor.json
in the v4 blindJudgments format (model="gpt-4o"). Key from ~/.hermes/.env.
"""
import json, os, re, sys, time, urllib.request, urllib.error

CONDITIONS = ["naked", "context", "psyche", "best"]
PERMS = [[0, 1, 2, 3], [3, 2, 1, 0], [1, 3, 0, 2], [2, 0, 3, 1], [0, 2, 1, 3], [3, 1, 2, 0]]
LETTERS = ["A", "B", "C", "D"]
MODEL = "gpt-4o"

def openai_key():
    env = open(os.path.expanduser("~/.hermes/.env")).read()
    m = re.search(r'^OPENAI_API_KEY=["\']?([^"\'\n]+)', env, re.M)
    return m.group(1).strip()

KEY = openai_key()

def judge(prompt):
    body = json.dumps({
        "model": MODEL,
        "messages": [{"role": "user", "content": prompt}],
        "response_format": {"type": "json_object"},
        "temperature": 0.3,
        "max_completion_tokens": 900,
    }).encode()
    for attempt in range(4):
        try:
            req = urllib.request.Request("https://api.openai.com/v1/chat/completions", data=body,
                headers={"Authorization": f"Bearer {KEY}", "Content-Type": "application/json"})
            r = json.load(urllib.request.urlopen(req, timeout=90))
            return json.loads(r["choices"][0]["message"]["content"])
        except urllib.error.HTTPError as e:
            if e.code == 429: time.sleep(5 * (attempt + 1)); continue
            print(f"  HTTP {e.code}: {e.read().decode()[:150]}"); return None
        except Exception as ex:
            print(f"  err: {str(ex)[:120]}"); time.sleep(3); continue
    return None

def judge_prompt(task, arr, permIdx):
    perm = PERMS[permIdx]
    block = "".join(f"\n--- ANSWER {LETTERS[k]} ---\n{arr[perm[k]]}\n" for k in range(4))
    return (f"Evaluate four answers to the same task. You do NOT know how any was produced. "
            f"Their order (A/B/C/D) is RANDOMIZED and carries no meaning — do NOT let position "
            f"influence you; rank strictly on content. "
            f"Judge PURELY on general quality: specificity, actionability, correctness, and genuine "
            f"insight/usefulness. Do not reward verbosity.\n\nTASK: {task}\n{block}\n"
            f'Return ONLY JSON: {{"ranking":["A|B|C|D" best-to-worst, all four], '
            f'"scores":{{"A":{{"specificity":n,"actionability":n,"correctness":n,"insight":n}},"B":{{...}},"C":{{...}},"D":{{...}}}}, '
            f'"rationale":"2 sentences"}}')

def main():
    base = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    v2 = json.load(open(os.path.join(base, "results/raw/results-v2.json")))
    out = []
    for tIdx, t in enumerate(v2):
        if t.get("checkable"):
            continue
        arr = [t["responses"][c] for c in CONDITIONS]
        judgments = []
        for j in range(6):  # all 6 perms/task → position bias fully averaged
            permIdx = j % len(PERMS)
            r = judge(judge_prompt(t["prompt"], arr, permIdx))
            if r and r.get("ranking") and len(r["ranking"]) == 4:
                judgments.append({"model": MODEL, "permIdx": permIdx,
                                  "ranking": r["ranking"], "scores": r.get("scores", {}),
                                  "rationale": r.get("rationale", "")})
                print(f"  {t['taskId']} j{j}: {r['ranking']}")
            else:
                print(f"  {t['taskId']} j{j}: FAILED")
        out.append({"taskId": t["taskId"], "checkable": False, "blindJudgments": judgments})
    dest = os.path.join(base, "results/raw/results-v6-crossvendor.json")
    json.dump(out, open(dest, "w"), indent=2)
    print(f"\nwrote {dest}: {sum(len(t['blindJudgments']) for t in out)} GPT-4o judgments over {len(out)} tasks")

if __name__ == "__main__":
    main()
