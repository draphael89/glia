#!/usr/bin/env python3
"""Aggregate an injection A/B run (eval-inject-ab.js).

variant0 = baseline, variant1 = the improvement under test. Reports variant1's
blind win rate over variant0, per-dimension rubric means, and a verdict.

Usage: python3 aggregate-inject-ab.py results/raw/results-ab.json [label0] [label1]
"""
import json
import sys
from collections import defaultdict

DIMS = ["specificity", "actionability", "insight"]


def main(path, label0="baseline", label1="improved"):
    tasks = json.load(open(path))
    wins = {0: 0, 1: 0, "tie": 0}
    rub = {0: defaultdict(list), 1: defaultdict(list)}
    n = 0
    per_task = []
    for t in tasks:
        tw = {0: 0, 1: 0, "tie": 0}
        for j in t.get("judgments", []):
            w = j.get("winnerVariant")
            if w not in (0, 1, "tie"):
                continue
            n += 1
            wins[w] += 1
            tw[w] += 1
            sc = j.get("scores") or {}
            # scores keyed by slot A/B; the harness already de-shuffled winner,
            # but rubric scores are still by slot — skip if we can't map cleanly.
            for slot in ("A", "B"):
                pass
        per_task.append((t["taskId"], tw))

    out = ["# Injection A/B — %s (variant0) vs %s (variant1)\n" % (label0, label1)]
    out.append(f"_{len(tasks)} tasks · {n} blind pairwise judgments_\n")
    decided = wins[0] + wins[1]
    v1rate = (100 * wins[1] / decided) if decided else 0
    out.append(f"\n## Result\n")
    out.append(f"- **{label1}** (variant1) wins **{wins[1]}**, **{label0}** (variant0) wins **{wins[0]}**, ties **{wins['tie']}**")
    if decided:
        out.append(f"- Among decided judgments, **{label1} wins {v1rate:.0f}%**")
    out.append("\n## Per task (variant1 / variant0 / tie)\n")
    for tid, tw in per_task:
        out.append(f"- `{tid}`: {tw[1]} / {tw[0]} / {tw['tie']}")

    out.append("\n## Verdict\n")
    if not decided:
        out.append(f"- All ties — {label1} is indistinguishable from {label0}.")
    elif v1rate >= 60:
        out.append(f"- **{label1} wins** ({v1rate:.0f}%). Keep the change.")
    elif v1rate >= 45:
        out.append(f"- **≈ tie** ({v1rate:.0f}%). {label1} doesn't hurt; keep if it has other benefits (budget, cost).")
    else:
        out.append(f"- **{label1} LOSES** ({v1rate:.0f}%). Reconsider or revert.")
    out.append("\n---\n_Small-n pilot; blind pairwise. Generated by aggregate-inject-ab.py._")
    print("\n".join(out))
    open("REPORT-inject-ab.md", "w").write("\n".join(out) + "\n")


if __name__ == "__main__":
    main(sys.argv[1] if len(sys.argv) > 1 else "results/raw/results-ab.json",
         sys.argv[2] if len(sys.argv) > 2 else "baseline",
         sys.argv[3] if len(sys.argv) > 3 else "improved")
