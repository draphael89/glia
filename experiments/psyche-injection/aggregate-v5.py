#!/usr/bin/env python3
"""Aggregate v5 — the dose-response run.

Does the identity lift saturate at a small psyche dose? For each task we
generated a psyche-arm answer at 4 truncation budgets (tiny ~700, small ~3k,
medium ~8k, full ~28k tokens) and blind-ranked them. If quality plateaus early,
the MCP's "concentrated identity core suffices" design (and its 40% cap) is
validated. If it keeps climbing with dose, more psyche is genuinely better.

Usage: python3 aggregate-v5.py [results/raw/results-v5.json]
"""
import json
import sys
from pathlib import Path
from collections import defaultdict

DOSES = ["tiny", "small", "medium", "full"]
TOKENS = {"tiny": 700, "small": 3000, "medium": 8000, "full": 28400}
PERMS = [[0, 1, 2, 3], [3, 2, 1, 0], [1, 3, 0, 2],
         [2, 0, 3, 1], [0, 2, 1, 3], [3, 1, 2, 0]]
LETTERS = ["A", "B", "C", "D"]
DIMS = ["specificity", "actionability", "correctness", "insight"]


def slot_map(perm):
    return {LETTERS[k]: DOSES[perm[k]] for k in range(4)}


def main(path):
    tasks = json.load(open(path))
    borda = {d: 0.0 for d in DOSES}
    rub = {d: {k: [] for k in DIMS} for d in DOSES}
    pw, pt = defaultdict(int), defaultdict(int)
    n = 0
    for t in tasks:
        for j in t.get("blindJudgments", []):
            r = j.get("ranking")
            if not r or len(r) != 4:
                continue
            n += 1
            sm = slot_map(PERMS[j.get("permIdx", 0)])
            ranked = [sm[L] for L in r]
            for rank, d in enumerate(ranked):
                borda[d] += (3 - rank)
            for a in range(4):
                for b in range(a + 1, 4):
                    pw[(ranked[a], ranked[b])] += 1
                    pt[(ranked[a], ranked[b])] += 1
                    pt[(ranked[b], ranked[a])] += 1
            sc = j.get("scores") or {}
            for L in LETTERS:
                for k in DIMS:
                    v = (sc.get(L) or {}).get(k)
                    if isinstance(v, (int, float)):
                        rub[sm[L]][k].append(float(v))

    out = ["# Psyche Injection — v5 (dose-response)\n"]
    out.append(f"_{len(tasks)} identity-shaped tasks · psyche truncated to 4 budgets · "
               f"{n} blind judgments · does the lift saturate?_\n")

    out.append("\n## Borda by dose (higher = judged better)\n")
    maxb = max(borda.values()) or 1
    for d in DOSES:
        out.append(f"- `{d:7}` (~{TOKENS[d]:>5} tok)  {borda[d]:5.0f}  {'█'*round(24*borda[d]/maxb)}")

    out.append("\n## Insight score by dose (the dimension identity drives)\n")
    for d in DOSES:
        xs = rub[d]["insight"]
        out.append(f"- `{d:7}` insight {sum(xs)/len(xs):.1f}" if xs else f"- `{d:7}` –")

    # saturation analysis: marginal Borda gain per dose step
    out.append("\n## Saturation\n")
    steps = []
    for i in range(1, len(DOSES)):
        lo, hi = DOSES[i-1], DOSES[i]
        steps.append((f"{lo}→{hi}", borda[hi] - borda[lo]))
    for name, gain in steps:
        out.append(f"- {name}: {'+' if gain>=0 else ''}{gain:.0f} Borda")
    best_dose = max(DOSES, key=lambda d: borda[d])
    # where does it get within 90% of the max?
    thresh = 0.9 * borda[best_dose]
    knee = next((d for d in DOSES if borda[d] >= thresh), best_dose)

    out.append("\n## Verdict\n")
    out.append(f"- Best dose: **{best_dose}** (~{TOKENS[best_dose]} tokens).")
    out.append(f"- Reaches 90% of peak quality by **{knee}** (~{TOKENS[knee]} tokens).")
    if knee in ("tiny", "small"):
        out.append(f"- **Saturates early.** A concentrated ~{TOKENS[knee]}-token identity core "
                   f"carries most of the lift — validates the MCP capping the psyche and "
                   f"handing the rest of the budget to retrieval. More psyche ≈ diminishing returns.")
    elif knee == "medium":
        out.append(f"- **Partial saturation.** Quality keeps improving up to ~{TOKENS['medium']} "
                   f"tokens, then flattens. The MCP's cap should leave room for a medium dose, "
                   f"not just a tiny core.")
    else:
        out.append(f"- **Does not saturate.** Quality climbs all the way to the full psyche — "
                   f"the 'a small dose suffices' claim does NOT hold; more identity is better, "
                   f"so the MCP should prefer a larger psyche share when the budget allows.")
    out.append("\n---\n_Generated by aggregate-v5.py. Small-n pilot._")
    Path("REPORT-v5.md").write_text("\n".join(out) + "\n")
    print("\n".join(out))


if __name__ == "__main__":
    main(sys.argv[1] if len(sys.argv) > 1 else "results/raw/results-v5.json")
