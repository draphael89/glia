#!/usr/bin/env python3
"""Aggregate v4 — the self-preference (cross-model judge) check.

The answers were generated by Opus 4.8. v4 re-judges the SAME fixed answers with
Haiku 4.5 and Fable 5 as blind judges. If a non-generator model reproduces the
best > context > psyche > naked ordering, the finding is a real signal, not Opus
preferring its own outputs.

Usage: python3 aggregate-v4.py [results/raw/results-v4.json]
"""
import json
import sys
from pathlib import Path
from collections import defaultdict

CONDITIONS = ["naked", "context", "psyche", "best"]
PERMS = [[0, 1, 2, 3], [3, 2, 1, 0], [1, 3, 0, 2],
         [2, 0, 3, 1], [0, 2, 1, 3], [3, 1, 2, 0]]
LETTERS = ["A", "B", "C", "D"]
DIMS = ["specificity", "actionability", "correctness", "insight"]
OPUS = {"borda_order": ["best", "context", "psyche", "naked"],
        "best_gt_context": 71}  # from v2+v3 merged, 49 judgments


def slot_map(perm):
    return {LETTERS[k]: CONDITIONS[perm[k]] for k in range(4)}


def analyze(tasks, model):
    borda = {c: 0.0 for c in CONDITIONS}
    pw, pt = defaultdict(int), defaultdict(int)
    rub = {c: {d: [] for d in DIMS} for c in CONDITIONS}
    n = 0
    for t in tasks:
        if t.get("checkable"):
            continue
        for j in t.get("blindJudgments", []):
            if j.get("model") != model:
                continue
            r = j.get("ranking")
            if not r or len(r) != 4:
                continue
            n += 1
            sm = slot_map(PERMS[j.get("permIdx", 0)])
            ranked = [sm[L] for L in r]
            for rank, c in enumerate(ranked):
                borda[c] += (3 - rank)
            for a in range(4):
                for b in range(a + 1, 4):
                    pw[(ranked[a], ranked[b])] += 1
                    pt[(ranked[a], ranked[b])] += 1
                    pt[(ranked[b], ranked[a])] += 1
            sc = j.get("scores") or {}
            for L in LETTERS:
                for d in DIMS:
                    v = (sc.get(L) or {}).get(d)
                    if isinstance(v, (int, float)):
                        rub[sm[L]][d].append(float(v))
    return borda, n, pw, pt, rub


def section(out, model, tasks):
    borda, n, pw, pt, rub = analyze(tasks, model)
    if n == 0:
        out.append(f"\n## {model}: no judgments\n")
        return None
    order = sorted(CONDITIONS, key=lambda x: -borda[x])
    out.append(f"\n## Judge = {model} ({n} blind judgments)\n")
    maxb = max(borda.values()) or 1
    for c in order:
        out.append(f"- `{c:8}` {borda[c]:5.0f}  {'█'*round(20*borda[c]/maxb)}")
    bc = 100 * pw[("best", "context")] / pt[("best", "context")] if pt[("best", "context")] else 0
    cp = 100 * pw[("context", "psyche")] / pt[("context", "psyche")] if pt[("context", "psyche")] else 0
    out.append(f"\n- best > context: {bc:.0f}%   ·   context > psyche: {cp:.0f}%")
    reproduces = order == OPUS["borda_order"]
    out.append(f"- ordering: {' > '.join(order)}  "
               f"→ {'**reproduces Opus** ✓' if reproduces else 'differs from Opus'}")
    return {"order": order, "reproduces": reproduces, "best_gt_context": bc, "n": n}


def main(path):
    tasks = json.load(open(path))
    out = ["# Psyche Injection — v4 (cross-model judge / self-preference check)\n"]
    out.append("_Answers generated by **Opus 4.8**; re-judged blind by **Haiku 4.5** and "
               "**Fable 5**. Opus (v2+v3) ordering: best > context > psyche > naked._\n")

    results = {}
    for m in ["haiku", "fable"]:
        results[m] = section(out, m, tasks)

    out.append("\n## Verdict\n")
    ran = {m: r for m, r in results.items() if r}
    missing = [m for m, r in results.items() if not r]
    reproduced = [m for m, r in ran.items() if r["reproduces"]]
    best_first = [m for m, r in ran.items() if r["order"][0] == "best"]
    naked_last = [m for m, r in ran.items() if r["order"][-1] == "naked"]
    tight = [m for m, r in ran.items() if r["best_gt_context"] < 60]

    if reproduced:
        out.append(f"- **Ordering reproduces under a different model.** "
                   f"{', '.join(reproduced)} (non-generator) independently ranks "
                   f"best > context > psyche > naked — the same order Opus produced. "
                   f"The result is **not simply Opus preferring its own outputs.**")
    if naked_last:
        out.append(f"- **The floor is robust:** naked is clearly worst for "
                   f"{', '.join(naked_last)} too.")
    if tight:
        out.append(f"- **But the best-vs-context margin is model-dependent.** For "
                   f"{', '.join(tight)}, best > context is only "
                   f"{ran[tight[0]]['best_gt_context']:.0f}% — a near-tie, vs Opus's "
                   f"{OPUS['best_gt_context']}%. A smaller judge sees the *floor* "
                   f"(naked worst, identity/retrieval lead) clearly but is less "
                   f"sensitive to the subtle *insight* lift identity adds on top of "
                   f"retrieval. Trust the ordering; don't over-trust the exact margin.")
    if missing:
        out.append(f"- _Incomplete: {', '.join(missing)} judges did not run "
                   f"(usage limit), so this rests on {', '.join(ran)} alone — one "
                   f"non-generator model, and a smaller one. Weaker than a two-model "
                   f"check; honest to say so._")
    out.append("\n_Note: Haiku 4.5 (and Fable 5) are still Anthropic models — a true "
               "cross-vendor judge (GPT/Gemini) would be stronger, but a different model "
               "already breaks the tightest generator==judge loop._")
    out.append("\n---\n_Generated by aggregate-v4.py._")
    Path("REPORT-v4.md").write_text("\n".join(out) + "\n")
    print("\n".join(out))


if __name__ == "__main__":
    main(sys.argv[1] if len(sys.argv) > 1 else "results/raw/results-v4.json")
