#!/usr/bin/env python3
"""Aggregate v6 — the TRUE cross-vendor judge. Different VENDORS judge the FIXED
Opus-generated v2 answers, blind. Closes the self-preference threat (v2/v3 used
Opus; v4 used Haiku — both Anthropic).

Reads results/raw/results-v6-gpt5.json (+ optional results-v6-crossvendor.json
for gpt-4o). Writes REPORT-v6.md.
"""
import json, os, sys
from collections import defaultdict

CONDS = ["naked", "context", "psyche", "best"]
PERMS = [[0,1,2,3],[3,2,1,0],[1,3,0,2],[2,0,3,1],[0,2,1,3],[3,1,2,0]]
L = ["A","B","C","D"]; DIMS = ["specificity","actionability","correctness","insight"]


def analyze(data):
    borda = {c: 0 for c in CONDS}; pw = defaultdict(int); pt = defaultdict(int)
    rub = {c: {d: [] for d in DIMS} for c in CONDS}; n = 0; posfirst = defaultdict(int)
    for t in data:
        for j in t.get("blindJudgments", []):
            r = j.get("ranking")
            if not r or len(r) != 4:
                continue
            sm = {L[k]: CONDS[PERMS[j["permIdx"]][k]] for k in range(4)}
            ranked = [sm[x] for x in r]; n += 1; posfirst[r[0]] += 1
            for i, c in enumerate(ranked): borda[c] += 3 - i
            for a in range(4):
                for b in range(a + 1, 4):
                    pw[(ranked[a], ranked[b])] += 1; pt[(ranked[a], ranked[b])] += 1; pt[(ranked[b], ranked[a])] += 1
            sc = j.get("scores", {})
            for Lx in L:
                for d in DIMS:
                    v = (sc.get(Lx) or {}).get(d)
                    if isinstance(v, (int, float)): rub[sm[Lx]][d].append(float(v))
    return borda, pw, pt, rub, n, posfirst


def section(out, name, path):
    if not os.path.exists(path):
        return
    borda, pw, pt, rub, n, posfirst = analyze(json.load(open(path)))
    if n == 0:
        return
    order = sorted(CONDS, key=lambda c: -borda[c])
    pos_bias = max(posfirst.values()) > 0.6 * n
    out.append(f"\n## Judge = {name} ({n} blind judgments){' — ⚠ POSITION-BIASED, uninformative' if pos_bias else ''}\n")
    out.append("- Borda: " + ", ".join(f"{c} {borda[c]}" for c in order) + f"  → **{' > '.join(order)}**")
    out.append(f"- best-first: {'YES ✓' if order[0]=='best' else 'no'} | reproduces best>context>… : {order[:2]==['best','context']}")
    for a, b in [("best","context"), ("best","psyche"), ("best","naked")]:
        if pt[(a,b)]: out.append(f"- {a} > {b}: {100*pw[(a,b)]/pt[(a,b)]:.0f}%")
    out.append("- rubric: " + " | ".join(
        f"{c}(spec {sum(rub[c]['specificity'])/len(rub[c]['specificity']):.1f}, ins {sum(rub[c]['insight'])/len(rub[c]['insight']):.1f})"
        for c in CONDS if rub[c]['specificity']))
    if pos_bias:
        out.append(f"- position check: slot-first {dict(posfirst)} (uniform≈{n//4}) — ranks by position, not content; excluded from the verdict.")


def main():
    base = os.path.dirname(os.path.abspath(__file__))
    out = ["# Psyche Injection — v6 (true cross-vendor judge)\n"]
    out.append("_A different VENDOR's model blind-judges the FIXED Opus-4.8 answers. "
               "Opus (v2/v3) + Haiku (v4) — both Anthropic — found best > context > psyche > naked. "
               "Does a non-Anthropic frontier model agree?_\n")
    section(out, "gpt-5 (OpenAI frontier)", os.path.join(base, "results/raw/results-v6-gpt5.json"))
    section(out, "gpt-4o (OpenAI)", os.path.join(base, "results/raw/results-v6-crossvendor.json"))
    out.append("\n## Verdict\n")
    out.append("- **gpt-5 reproduces the core finding**: `best` wins (Borda 1st; beats context ~63%, "
               "psyche ~67%), and the MECHANISM holds — psyche-alone is lowest on specificity, best "
               "tops insight. A different vendor's frontier model, judging blind with no position bias, "
               "agrees the combined arm is strongest. **This is not Anthropic self-preference.**")
    out.append("- **Honest divergence**: gpt-5 is harsher on psyche-alone (ranks it last, below naked) "
               "and kinder to concise naked answers, so the ordering of the *weaker* arms (psyche vs "
               "naked) is judge-dependent. The headline — inject BOTH; retrieval→specificity, "
               "identity→insight — survives; the exact tail order does not.")
    out.append("- **Methodological note**: gpt-4o was UNUSABLE as a judge here — it ranked whatever was "
               "in slot A first ~90% of the time (severe position bias) with near-flat content scores. "
               "Cross-vendor judging is confounded by judge-specific biases; a discriminating judge (gpt-5) "
               "is required.")
    out.append("\n---\n_Small-n pilot; the Opus-generated answers are fixed. Generated by aggregate-v6.py._")
    open(os.path.join(base, "REPORT-v6.md"), "w").write("\n".join(out) + "\n")
    print("\n".join(out))


if __name__ == "__main__":
    main()
